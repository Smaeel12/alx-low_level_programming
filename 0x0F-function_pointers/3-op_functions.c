/**
 * op_add - calculate the addition.
 * @a: first number
 * @b: second number
 * Return: result.
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - calculate the subtraction.
 * @a: first number
 * @b: second number
 * Return: result.
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - calculate the multiplication.
 * @a: first number
 * @b: second number
 * Return: result.
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - calculate the division.
 * @a: first number
 * @b: second number
 * Return: result.
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - calculate the modulo.
 * @a: first number
 * @b: second number
 * Return: result.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
