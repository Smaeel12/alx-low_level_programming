/**
 * add - function that calculate the sum of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function that calculate the substraction of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function that calculate the multiplication of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - function that calculate the multiplication of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function that calculate the module of two numbers.
 * @a: first number.
 * @b: second number.
 * Return: the result
 */
int mod(int a, int b)
{
	return (a % b);
}
