#include "ops.h"
/**
 * add - add function
 * @a: first integer
 * @b: second integer
 * Return: the result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub function
 * @a: first integer
 * @b: second integer
 * Return: the result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul function
 * @a: first integer
 * @b: second integer
 * Return: the result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - div function
 * @a: first integer
 * @b: second integer
 * Return: the result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod function
 * @a: first integer
 * @b: second integer
 * Return: the result
 */
int mod(int a, int b)
{
	return (a % b);
}
